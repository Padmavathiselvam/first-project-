package org.testing;

public class Forloop {

	public static void main(String[] args) {
		
		
		for (int i = 0; i <=5; i++) { // outer loop 
			
			for (int j = 0; j <=5; j++) { // inner loop
				
				System.out.print("$");
			}
			
			System.out.println();
		}
		
		
		
		
		
		
		
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
